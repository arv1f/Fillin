import type { ScreenCornerQuad } from '../lib/panoProject'
import type { SceneId } from '../scenes'

/** Привязка к панораме (yaw/pitch) — зона двигается при вращении. */
export type PanoHotspotSphere = {
  mode?: 'sphere'
  to: SceneId
  yaw: number
  pitch: number
  yawHalfSpan: number
  pitchHalfSpan: number
  maxWidthPx?: number
  maxHeightPx?: number
  label?: string
}

/**
 * Прямоугольник в плоскости вьюпорта: четыре точки (x,y) в долях 0..1 от ширины/высоты
 * области панорамы. Bbox по min/max — при вращении 360° положение на экране не меняется.
 */
export type PanoHotspotScreen = {
  mode: 'screen'
  to: SceneId
  corners: ScreenCornerQuad
  label?: string
}

export type PanoHotspot = PanoHotspotSphere | PanoHotspotScreen

export function isScreenHotspot(h: PanoHotspot): h is PanoHotspotScreen {
  return h.mode === 'screen'
}

